num = int(input("Ingrese un número: "))
if num % 4 == 0:
    print(f"El número es divisible por 4")
else:
    print(f"El número no es divisible por 4")
if num % 6 == 0:
    print(f"El número es divisible por 6")
else:
    print(f"El número no es divisible por 6")
if num % 8 == 0:
    print(f"El número es divisible por 8")
else:
    print(f"El número no es divisible por 8")
if num % 10 == 0:
    print(f"El número es divisible por 10")
else:
    print(f"El número no es divisible por 10")
